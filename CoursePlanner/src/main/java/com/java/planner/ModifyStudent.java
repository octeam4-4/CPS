    private StudentVO load(String studentId){
        Iterator<StudentVO> it = CoursePlanner.studentList.iterator();
       while(it.hasNext()){
           StudentVO studentVO = it.next();
           if(studentId.equals(studentVO.getId())){
                jTextField9.setText(studentVO.getId());
               jTextField7.setText(studentVO.getDegree());
              jTextField4.setText(studentVO.getGradDate());
             return studentVO;
           }
       }
       return null;
    }  
         
   

